function deepCopyTroop(troopTemplate) {
  var copy = [];
  for (var i = 0; i < troopTemplate.length; i++) {
    var val = troopTemplate[i];
    if (Array.isArray(val)) {
      var arrCopy = [];
      for (var j = 0; j < val.length; j++) {
        if (Array.isArray(val[j])) {
          arrCopy.push(val[j].slice());
        } else {
          arrCopy.push(val[j]);
        }
      }
      copy.push(arrCopy);
    } else {
      copy.push(val);
    }
  }
  return copy;
}